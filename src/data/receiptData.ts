// データを複製して80件に拡張する関数
function generateExtendedData() {
  const extended = [...receiptData];
  const baseDate = new Date('2026-02-27');
  
  // 残り72件を生成 (80 - 8 = 72)
  for (let i = 0; i < 72; i++) {
    const dayOffset = Math.floor(i / 10);  // 10件ごとに1日前
    const currentDate = new Date(baseDate);
    currentDate.setDate(currentDate.getDate() - dayOffset);
    
    const dateStr = currentDate.toISOString().split('T')[0].replace(/-/g, '/');
    const timeHour = 20 - (i % 10);  // 時間を変える
    const receiptType = 
      i % 15 === 0 ? "通常伝票（訂正）" :
      i % 20 === 0 ? "赤伝票（未訂正）" :
      i % 25 === 0 ? "訂正履歴" :
      "通常伝票";
    
    const symbols = ["01", "02", "03", "04", "05", "T1", "T2"];
    const symbol = symbols[i % symbols.length];
    
    const subtotal = 1000 + (i * 157) % 7000;
    const total = subtotal;
    const customers = 1 + (i % 4);
    const orders = 1 + (i % 3);
    
    // 支払い方法をランダムに
    let paymentData: any = {
      cash: 0,
      paypay: 0,
      voucher: 0,
      rakuten: 0,
      square: 0,
      urikake: 0,
    };
    
    const paymentType = i % 6;
    if (paymentType === 0) paymentData.cash = total;
    else if (paymentType === 1) paymentData.paypay = total;
    else if (paymentType === 2) paymentData.rakuten = total;
    else if (paymentType === 3) paymentData.square = total;
    else if (paymentType === 4) paymentData.urikake = total;
    else paymentData.voucher = total;
    
    extended.push({
      dataGenTime: `${dateStr} ${timeHour.toString().padStart(2, '0')}:${(i % 60).toString().padStart(2, '0')}:${((i * 13) % 60).toString().padStart(2, '0')}`,
      date: dateStr,
      receiptNo: `M1772${1000000 + i}`,
      type: receiptType,
      symbol: symbol,
      orderType: "TableCode",
      moPayment: "0",
      moStatus: "利用なし",
      checkinTime: `${dateStr} ${(timeHour - 1).toString().padStart(2, '0')}:${((i + 5) % 60).toString().padStart(2, '0')}:${((i * 7) % 60).toString().padStart(2, '0')}`,
      checkoutTime: `${dateStr} ${timeHour.toString().padStart(2, '0')}:${(i % 60).toString().padStart(2, '0')}:${((i * 13) % 60).toString().padStart(2, '0')}`,
      subtotal: subtotal,
      discount: 0,
      total: total,
      customers: customers,
      orders: orders,
      ...paymentData,
      change: 0,
      funfo: 0,
      stera: 0,
      squareTerminal: 0,
      point: 0,
    });
  }
  
  return extended;
}

export const extendedReceiptData = generateExtendedData();
